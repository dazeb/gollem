func typeScriptType(value any, indent int) (string, error) {
	schema, ok := typeScriptSchema(value)
	if !ok || len(schema) == 0 {
		return "unknown", nil
	}
	if bigint, _ := schema[typeScriptBigIntKeyword].(bool); bigint {
		if schema["type"] == "integer" {
			return "bigint", nil
		}
		types, ok := typeScriptAnySlice(schema["type"])
		if !ok {
			return "", errors.New("TypeScript bigint schema must have integer type")
		}
		parts := make([]string, 0, len(types))
		for _, value := range types {
			switch value {
			case "integer":
				parts = appendUniqueTypeScript(parts, "bigint")
			case "null":
				parts = appendUniqueTypeScript(parts, "null")
			default:
				return "", errors.New("TypeScript bigint schema type array must contain only integer and null")
			}
		}
		if !slices.Contains(parts, "bigint") {
			return "", errors.New("TypeScript bigint schema type array must contain integer")
		}
		return strings.Join(parts, " | "), nil
	}
	if ref, ok := schema["$ref"].(string); ok {
		const prefix = "#/$defs/"
		if !strings.HasPrefix(ref, prefix) || len(ref) == len(prefix) {
			return "", fmt.Errorf("unsupported ref %q", ref)
		}
		return strings.TrimPrefix(ref, prefix), nil
	}
	if values, ok := typeScriptAnySlice(schema["enum"]); ok {
		return typeScriptLiteralUnion(values), nil
	}
	for _, keyword := range []string{"oneOf", "anyOf"} {
		if variants, ok := typeScriptAnySlice(schema[keyword]); ok {
			parts := make([]string, 0, len(variants))
			for _, variant := range variants {
				part, err := typeScriptType(variant, indent)
				if err != nil {
					return "", err
				}
				parts = appendUniqueTypeScript(parts, part)
			}
			if len(parts) == 0 {
				return "never", nil
			}
			return strings.Join(parts, " | "), nil
		}
	}

	typeName, _ := schema["type"].(string)
	switch typeName {
	case "string":
		return "string", nil
	case "integer", "number":
		return "number", nil
	case "boolean":
		return "boolean", nil
	case "null":
		return "null", nil
	case "array":
		item, err := typeScriptType(schema["items"], indent)
		if err != nil {
			return "", err
		}
		return "Array<" + item + ">", nil
	case "object":
		return typeScriptObjectType(schema, indent)
	case "":
		return "unknown", nil
	default:
		return "", fmt.Errorf("unsupported schema type %q", typeName)
	}
}

func typeScriptObjectType(schema Schema, indent int) (string, error) {
	properties, _ := typeScriptSchema(schema["properties"])
	required := typeScriptRequiredNames(schema["required"])
	additional, additionalIsSchema := typeScriptSchema(schema["additionalProperties"])
	additionalAllowed := true
	if allowed, ok := schema["additionalProperties"].(bool); ok {
		additionalAllowed = allowed
	}
	if len(properties) == 0 {
		if additionalIsSchema {
			valueType, err := typeScriptType(additional, indent)
			if err != nil {
				return "", err
			}
			if recursive, _ := schema["x-gollem-typescript-recursive-map"].(bool); recursive {
				return "{ [key: string]: " + valueType + " }", nil
			}
			if optional, _ := schema["x-gollem-typescript-optional-map"].(bool); optional {
				return "{ [key in string]?: " + valueType + " }", nil
			}
			return "Record<string, " + valueType + ">", nil
		}
		if !additionalAllowed {
			return "Record<string, never>", nil
		}
		return "Record<string, unknown>", nil
	}

	names := make([]string, 0, len(properties))
	for name := range properties {
		names = append(names, name)
	}
	sort.Strings(names)
	baseIndent := strings.Repeat("  ", indent)
	fieldIndent := strings.Repeat("  ", indent+1)
	var out strings.Builder
	out.WriteString("{\n")
	for _, name := range names {
		fieldValue := properties[name]
		fieldSchema, _ := typeScriptSchema(fieldValue)
		var fieldType string
		var err error
		if optionalNonNull, _ := fieldSchema[typeScriptOptionalNonNullKeyword].(bool); optionalNonNull {
			fieldType, err = typeScriptOptionalNonNullType(fieldSchema, indent+1)
		} else {
			fieldType, err = typeScriptType(fieldValue, indent+1)
		}
		if err != nil {
			return "", fmt.Errorf("property %s: %w", name, err)
		}
		optional := "?"
		if required[name] {
			optional = ""
		}
		fmt.Fprintf(&out, "%s%s%s: %s;\n", fieldIndent, typeScriptString(name), optional, fieldType)
	}
	out.WriteString(baseIndent)
	out.WriteByte('}')
	objectType := out.String()
	if additionalIsSchema {
		valueType, err := typeScriptType(additional, indent)
		if err != nil {
			return "", err
		}
		additionalType := "Record<string, " + valueType + ">"
		if optional, _ := schema["x-gollem-typescript-optional-map"].(bool); optional {
			additionalType = "{ [key in string]?: " + valueType + " }"
		}
		return "(" + objectType + " & " + additionalType + ")", nil
	}
	ignoreAdditional, _ := schema["x-gollem-typescript-ignore-additional-properties"].(bool)
	if additionalAllowed && !ignoreAdditional {
		return "(" + objectType + " & Record<string, unknown>)", nil
	}
	return objectType, nil
}

func typeScriptOptionalNonNullType(schema Schema, indent int) (string, error) {
	variants, ok := typeScriptAnySlice(schema["anyOf"])
	if !ok {
		return "", errors.New("optional non-null TypeScript schema requires anyOf")
	}
	nonNull := make([]any, 0, len(variants))
	for _, variant := range variants {
		variantSchema, _ := typeScriptSchema(variant)
		if variantSchema["type"] == "null" {
			continue
		}
		nonNull = append(nonNull, variant)
	}
	if len(nonNull) == 0 {
		return "", errors.New("optional non-null TypeScript schema has no non-null variant")
	}
	if len(nonNull) == 1 {
		return typeScriptType(nonNull[0], indent)
	}
	return typeScriptType(Schema{"anyOf": nonNull}, indent)
}

func typeScriptSchema(value any) (Schema, bool) {
	switch typed := value.(type) {
	case Schema:
		return typed, true
	case map[string]any:
		return Schema(typed), true
	default:
		return nil, false
	}
}

func typeScriptAnySlice(value any) ([]any, bool) {
	typed, ok := value.([]any)
	return typed, ok
}

func typeScriptRequiredNames(value any) map[string]bool {
	out := make(map[string]bool)
	switch typed := value.(type) {
	case []string:
		for _, name := range typed {
			out[name] = true
		}
	case []any:
		for _, item := range typed {
			if name, ok := item.(string); ok {
				out[name] = true
			}
		}
	}
	return out
}

func typeScriptLiteralUnion(values []any) string {
	parts := make([]string, 0, len(values))
	for _, value := range values {
		encoded, err := json.Marshal(value)
		if err != nil {
			parts = append(parts, "unknown")
			continue
		}
		parts = appendUniqueTypeScript(parts, string(encoded))
	}
	if len(parts) == 0 {
		return "never"
	}
	return strings.Join(parts, " | ")
}

func appendUniqueTypeScript(values []string, value string) []string {
	for _, existing := range values {
		if existing == value {
			return values
		}
	}
	return append(values, value)
}

func typeScriptTypeUnion(names []string, empty string) string {
	if len(names) == 0 {
		return empty
	}
	return strings.Join(names, " | ")
}

func typeScriptString(value string) string {
	encoded, _ := json.Marshal(value)
	return string(encoded)
}

func typeScriptStringArray(values []string) string {
	encoded := make([]string, 0, len(values))
	for _, value := range values {
		encoded = append(encoded, typeScriptString(value))
	}
	return "[" + strings.Join(encoded, ", ") + "]"
}

