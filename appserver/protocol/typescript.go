// MarshalTypeScriptFixture converts the versioned JSON wire examples into a
// TypeScript compile fixture using the generated method and payload bindings.
func MarshalTypeScriptFixture(data []byte) ([]byte, error) {
	decoder := json.NewDecoder(bytes.NewReader(data))
	decoder.DisallowUnknownFields()
	var fixture typeScriptFixtureDocument
	if err := decoder.Decode(&fixture); err != nil {
		return nil, fmt.Errorf("decode runtime wire fixture: %w", err)
	}
	if fixture.ProtocolVersion != ProtocolVersion || fixture.SchemaVersion != SchemaVersion {
		return nil, fmt.Errorf("fixture versions %s/%s do not match %s/%s", fixture.ProtocolVersion, fixture.SchemaVersion, ProtocolVersion, SchemaVersion)
	}

	imports := map[string]bool{
		"BoundNotification": true,
		"BoundRequest":      true,
		"BoundResponse":     true,
	}
	for _, fixtureCase := range fixture.Cases {
		for _, name := range []string{fixtureCase.ParamsType, fixtureCase.ResultType, fixtureCase.PayloadType} {
			if name != "" {
				imports[name] = true
			}
		}
	}
	importNames := make([]string, 0, len(imports))
	for name := range imports {
		importNames = append(importNames, name)
	}
	sort.Strings(importNames)

	var out bytes.Buffer
	out.WriteString(typeScriptGeneratedHeader)
	out.WriteString("import type {\n")
	for _, name := range importNames {
		fmt.Fprintf(&out, "  %s,\n", name)
	}
	out.WriteString("} from \"../gollem_appserver_protocol\";\n\n")
	fmt.Fprintf(&out, "export const fixtureProtocolVersion = %s as const;\n", typeScriptString(fixture.ProtocolVersion))
	fmt.Fprintf(&out, "export const fixtureSchemaVersion = %s as const;\n\n", typeScriptString(fixture.SchemaVersion))

	seen := make(map[string]bool, len(fixture.Cases))
	hasInitialize := false
	hasInitialized := false
	hasFilesystem := false
	for _, fixtureCase := range fixture.Cases {
		identifier := typeScriptIdentifier(fixtureCase.Name)
		if identifier == "" || seen[identifier] {
			return nil, fmt.Errorf("invalid or duplicate fixture identifier for %q", fixtureCase.Name)
		}
		seen[identifier] = true
		info, ok := LookupMethod(fixtureCase.Method)
		if !ok || info.Surface != fixtureCase.Surface {
			return nil, fmt.Errorf("fixture %s references invalid method surface %s/%s", fixtureCase.Name, fixtureCase.Surface, fixtureCase.Method)
		}
		if !typeScriptFixtureBindingExists(fixtureCase) {
			return nil, fmt.Errorf("fixture %s type metadata is not bound to %s", fixtureCase.Name, fixtureCase.Method)
		}
		hasInitialize = hasInitialize || fixtureCase.Method == "initialize"
		hasInitialized = hasInitialized || fixtureCase.Method == "initialized"
		hasFilesystem = hasFilesystem || strings.HasPrefix(fixtureCase.Method, "fs/")

		var message bytes.Buffer
		if err := json.Indent(&message, fixtureCase.Message, "", "  "); err != nil {
			return nil, fmt.Errorf("format fixture %s: %w", fixtureCase.Name, err)
		}
		envelope, err := typeScriptFixtureEnvelope(fixtureCase)
		if err != nil {
			return nil, fmt.Errorf("fixture %s: %w", fixtureCase.Name, err)
		}
		bindingType := ""
		switch envelope {
		case "notification":
			bindingType = "BoundNotification<" + typeScriptString(fixtureCase.Method) + ">"
		case "request":
			bindingType = "BoundRequest<" + typeScriptString(fixtureCase.Method) + ">"
		case "response":
			bindingType = "BoundResponse<" + typeScriptString(fixtureCase.Method) + ">"
		}
		fmt.Fprintf(&out, "export const %s = %s satisfies %s;\n", identifier, message.String(), bindingType)
		if fixtureCase.ParamsType != "" {
			fmt.Fprintf(&out, "export const %sParams = %s.params satisfies %s;\n", identifier, identifier, fixtureCase.ParamsType)
		}
		if fixtureCase.ResultType != "" {
			fmt.Fprintf(&out, "export const %sResult = %s.result satisfies %s;\n", identifier, identifier, fixtureCase.ResultType)
		}
		if fixtureCase.PayloadType != "" {
			payload, err := typeScriptFixturePayload(fixtureCase.Message)
			if err != nil {
				return nil, fmt.Errorf("fixture %s payload: %w", fixtureCase.Name, err)
			}
			fmt.Fprintf(&out, "export const %sPayload = %s satisfies %s;\n", identifier, payload, fixtureCase.PayloadType)
		}
		out.WriteByte('\n')
	}
	out.WriteString("// @ts-expect-error request methods cannot use notification envelopes.\n")
	out.WriteString("export type RejectRequestAsNotification = BoundNotification<\"daemon/status\">;\n")
	out.WriteString("// @ts-expect-error notification methods cannot use request envelopes.\n")
	out.WriteString("export type RejectNotificationAsRequest = BoundRequest<\"item/started\">;\n")
	if hasInitialize {
		out.WriteString("// @ts-expect-error initialize requires clientInfo.\n")
		out.WriteString("export const rejectInitializeWithoutClientInfo = { \"capabilities\": {} } satisfies InitializeParams;\n")
		out.WriteString("// @ts-expect-error initialize requires clientInfo.version.\n")
		out.WriteString("export const rejectInitializeWithoutClientVersion = { \"clientInfo\": { \"name\": \"client\" } } satisfies InitializeParams;\n")
	}
	if hasInitialized {
		out.WriteString("// @ts-expect-error initialized does not accept params.\n")
		out.WriteString("export const rejectInitializedParams = { \"method\": \"initialized\", \"params\": {} } satisfies BoundNotification<\"initialized\">;\n")
	}
	if hasFilesystem {
		out.WriteString("// @ts-expect-error public writes require base64 data.\n")
		out.WriteString("export const rejectFilesystemWriteWithoutData = { path: \"/workspace/file\" } satisfies FsWriteFileParams;\n")
		out.WriteString("// @ts-expect-error copy recursive is non-null when present.\n")
		out.WriteString("export const rejectFilesystemCopyNullRecursive = { sourcePath: \"/workspace/a\", destinationPath: \"/workspace/b\", recursive: null } satisfies FsCopyParams;\n")
		out.WriteString("// @ts-expect-error directory responses require a non-null entries array.\n")
		out.WriteString("export const rejectFilesystemNullEntries = { entries: null } satisfies FsReadDirectoryResponse;\n")
		out.WriteString("// @ts-expect-error watch notifications require changedPaths.\n")
		out.WriteString("export const rejectFilesystemChangedWithoutPaths = { watchId: \"watch\" } satisfies FsChangedNotification;\n")
		out.WriteString("// @ts-expect-error metadata responses require every public field.\n")
		out.WriteString("export const rejectFilesystemPartialMetadata = { isFile: true } satisfies FsGetMetadataResponse;\n")
	}
	return out.Bytes(), nil
}

func typeScriptFixtureEnvelope(fixtureCase typeScriptFixtureCase) (string, error) {
	envelope := fixtureCase.Envelope
	if envelope == "" {
		switch fixtureCase.Surface {
		case SurfaceServerNotification, SurfaceClientNotification:
			envelope = "notification"
		case SurfaceServerRequest, SurfaceClientRequest:
			envelope = "request"
		case SurfaceGollemExtension:
			envelope = "response"
		}
	}

	valid := false
	switch envelope {
	case "notification":
		valid = fixtureCase.Surface == SurfaceServerNotification || fixtureCase.Surface == SurfaceClientNotification
	case "request", "response":
		valid = fixtureCase.Surface == SurfaceClientRequest || fixtureCase.Surface == SurfaceServerRequest || fixtureCase.Surface == SurfaceGollemExtension
	default:
		return "", fmt.Errorf("unsupported envelope %q", envelope)
	}
	if !valid {
		return "", fmt.Errorf("envelope %q is incompatible with surface %q", envelope, fixtureCase.Surface)
	}
	return envelope, nil
}

func typeScriptFixturePayload(message json.RawMessage) (string, error) {
	var envelope struct {
		Params struct {
			Item struct {
				Payload json.RawMessage `json:"payload"`
			} `json:"item"`
		} `json:"params"`
	}
	if err := json.Unmarshal(message, &envelope); err != nil {
		return "", err
	}
	if len(envelope.Params.Item.Payload) == 0 {
		return "", errors.New("params.item.payload is missing")
	}
	var formatted bytes.Buffer
	if err := json.Indent(&formatted, envelope.Params.Item.Payload, "", "  "); err != nil {
		return "", err
	}
	return formatted.String(), nil
}

func typeScriptFixtureBindingExists(fixtureCase typeScriptFixtureCase) bool {
	for _, binding := range WireTypeBindings() {
		if binding.Method != fixtureCase.Method || binding.Surface != fixtureCase.Surface {
			continue
		}
		if fixtureCase.ParamsType != "" && !stringSliceContains(binding.Params, fixtureCase.ParamsType) {
			return false
		}
		if fixtureCase.ResultType != "" && !stringSliceContains(binding.Result, fixtureCase.ResultType) {
			return false
		}
		if fixtureCase.PayloadType != "" {
			for _, payloadBinding := range ItemPayloadBindings() {
				if payloadBinding.Type == fixtureCase.PayloadType {
					return true
				}
			}
			return false
		}
		return true
	}
	return false
}

func stringSliceContains(values []string, want string) bool {
	for _, value := range values {
		if value == want {
			return true
		}
	}
	return false
}

func typeScriptIdentifier(value string) string {
	var out strings.Builder
	upperNext := false
	for i, r := range value {
		if !unicode.IsLetter(r) && !unicode.IsDigit(r) {
			upperNext = out.Len() > 0
			continue
		}
		if out.Len() == 0 {
			if unicode.IsDigit(r) {
				out.WriteByte('_')
			}
			out.WriteRune(unicode.ToLower(r))
			continue
		}
		if upperNext {
			r = unicode.ToUpper(r)
			upperNext = false
		} else if i == 0 {
			r = unicode.ToLower(r)
		}
		out.WriteRune(r)
	}
	return out.String()
}
