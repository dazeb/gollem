func writeTypeScriptProtocolMetadata(out *bytes.Buffer) {
	out.WriteString("export type ProtocolSurface =\n")
	for i, surface := range []Surface{
		SurfaceClientRequest,
		SurfaceServerNotification,
		SurfaceServerRequest,
		SurfaceClientNotification,
		SurfaceGollemExtension,
	} {
		terminator := "\n"
		if i == 4 {
			terminator = ";\n\n"
		}
		fmt.Fprintf(out, "  | %s%s", typeScriptString(string(surface)), terminator)
	}
	out.WriteString("export type ProtocolMethodState =\n")
	states := []MethodState{
		MethodImplemented,
		MethodBlocked,
		MethodDeferredStub,
		MethodRenamedEquivalent,
		MethodNotApplicable,
	}
	for i, state := range states {
		terminator := "\n"
		if i == len(states)-1 {
			terminator = ";\n\n"
		}
		fmt.Fprintf(out, "  | %s%s", typeScriptString(string(state)), terminator)
	}
	out.WriteString("export type ProtocolMethodInfo = {\n")
	out.WriteString("  \"method\": string;\n")
	out.WriteString("  \"surface\": ProtocolSurface;\n")
	out.WriteString("  \"state\": ProtocolMethodState;\n")
	out.WriteString("  \"source\"?: string;\n")
	out.WriteString("};\n\n")

	methods := Methods()
	out.WriteString("export const protocolMethods = [\n")
	for _, method := range methods {
		fmt.Fprintf(out, "  { \"method\": %s, \"surface\": %s, \"state\": %s", typeScriptString(method.Method), typeScriptString(string(method.Surface)), typeScriptString(string(method.State)))
		if method.Source != "" {
			fmt.Fprintf(out, ", \"source\": %s", typeScriptString(method.Source))
		}
		out.WriteString(" },\n")
	}
	out.WriteString("] as const satisfies readonly ProtocolMethodInfo[];\n\n")

	writeTypeScriptMethodUnion(out, "ClientRequestMethod", MethodsBySurface(SurfaceClientRequest))
	writeTypeScriptMethodUnion(out, "ServerNotificationMethod", MethodsBySurface(SurfaceServerNotification))
	writeTypeScriptMethodUnion(out, "ServerRequestMethod", MethodsBySurface(SurfaceServerRequest))
	writeTypeScriptMethodUnion(out, "ClientNotificationMethod", MethodsBySurface(SurfaceClientNotification))
	writeTypeScriptMethodUnion(out, "GollemExtensionMethod", MethodsBySurface(SurfaceGollemExtension))
	out.WriteString("export type KnownMethod =\n")
	out.WriteString("  | ClientRequestMethod\n")
	out.WriteString("  | ServerNotificationMethod\n")
	out.WriteString("  | ServerRequestMethod\n")
	out.WriteString("  | ClientNotificationMethod\n")
	out.WriteString("  | GollemExtensionMethod;\n\n")
}

func writeTypeScriptMethodUnion(out *bytes.Buffer, name string, methods []MethodInfo) {
	fmt.Fprintf(out, "export type %s =", name)
	if len(methods) == 0 {
		out.WriteString(" never;\n\n")
		return
	}
	out.WriteByte('\n')
	for i, method := range methods {
		terminator := "\n"
		if i == len(methods)-1 {
			terminator = ";\n\n"
		}
		fmt.Fprintf(out, "  | %s%s", typeScriptString(method.Method), terminator)
	}
}

func writeTypeScriptWireBindings(out *bytes.Buffer) {
	bindings := WireTypeBindings()
	out.WriteString("export type WireTypeBinding = {\n")
	out.WriteString("  \"method\": KnownMethod;\n")
	out.WriteString("  \"surface\": ProtocolSurface;\n")
	out.WriteString("  \"params\"?: readonly string[];\n")
	out.WriteString("  \"result\"?: readonly string[];\n")
	out.WriteString("};\n\n")
	out.WriteString("export const wireTypeBindings = [\n")
	for _, binding := range bindings {
		fmt.Fprintf(out, "  { \"method\": %s, \"surface\": %s", typeScriptString(binding.Method), typeScriptString(string(binding.Surface)))
		if len(binding.Params) > 0 {
			fmt.Fprintf(out, ", \"params\": %s", typeScriptStringArray(binding.Params))
		}
		if len(binding.Result) > 0 {
			fmt.Fprintf(out, ", \"result\": %s", typeScriptStringArray(binding.Result))
		}
		out.WriteString(" },\n")
	}
	out.WriteString("] as const satisfies readonly WireTypeBinding[];\n\n")

	out.WriteString("export interface MethodParamsByName {\n")
	for _, binding := range bindings {
		fmt.Fprintf(out, "  %s: %s;\n", typeScriptString(binding.Method), typeScriptTypeUnion(binding.Params, "undefined"))
	}
	out.WriteString("}\n\n")
	out.WriteString("export interface MethodResultsByName {\n")
	for _, binding := range bindings {
		if len(binding.Result) == 0 {
			continue
		}
		fmt.Fprintf(out, "  %s: %s;\n", typeScriptString(binding.Method), typeScriptTypeUnion(binding.Result, "never"))
	}
	out.WriteString("}\n\n")
	out.WriteString("export type BoundRequestMethod = Extract<\n")
	out.WriteString("  keyof MethodParamsByName,\n")
	out.WriteString("  ClientRequestMethod | ServerRequestMethod | GollemExtensionMethod\n")
	out.WriteString(">;\n")
	out.WriteString("export type BoundNotificationMethod = Extract<\n")
	out.WriteString("  keyof MethodParamsByName,\n")
	out.WriteString("  ServerNotificationMethod | ClientNotificationMethod\n")
	out.WriteString(">;\n\n")
	out.WriteString("export type ParamsFor<M extends keyof MethodParamsByName> = MethodParamsByName[M];\n")
	out.WriteString("export type ResultFor<M extends keyof MethodResultsByName> = MethodResultsByName[M];\n\n")
	out.WriteString("export type BoundRequest<M extends BoundRequestMethod> =\n")
	out.WriteString("  MethodParamsByName[M] extends undefined\n")
	out.WriteString("    ? { \"id\": RequestID; \"method\": M; \"params\"?: never }\n")
	out.WriteString("    : { \"id\": RequestID; \"method\": M; \"params\": MethodParamsByName[M] };\n\n")
	out.WriteString("export type BoundNotification<M extends BoundNotificationMethod> =\n")
	out.WriteString("  MethodParamsByName[M] extends undefined\n")
	out.WriteString("    ? { \"method\": M; \"params\"?: never }\n")
	out.WriteString("    : { \"method\": M; \"params\": MethodParamsByName[M] };\n\n")
	out.WriteString("export type BoundResponse<M extends keyof MethodResultsByName> =\n")
	out.WriteString("  | { \"id\": RequestID; \"result\": MethodResultsByName[M]; \"error\"?: never }\n")
	out.WriteString("  | { \"id\": RequestID; \"result\"?: never; \"error\": Error };\n\n")
}

